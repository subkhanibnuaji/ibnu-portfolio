const CATEGORIES = Object.keys(EMOJI_CATEGORIES)

export function EmojiPicker() {
  const [search, setSearch] = useState('')
  const [activeCategory, setActiveCategory] = useState('Smileys')
  const [copied, setCopied] = useState<string | null>(null)
  const [recentEmojis, setRecentEmojis] = useState<string[]>([])
  const [favorites, setFavorites] = useState<string[]>([])

  useEffect(() => {
    const savedRecent = localStorage.getItem('emojiRecent')
    const savedFavorites = localStorage.getItem('emojiFavorites')
    if (savedRecent) setRecentEmojis(JSON.parse(savedRecent))
    if (savedFavorites) setFavorites(JSON.parse(savedFavorites))
  }, [])

  const filteredEmojis = useMemo(() => {
    if (!search) return EMOJI_CATEGORIES[activeCategory as keyof typeof EMOJI_CATEGORIES] || []

    const searchLower = search.toLowerCase()
    const results: string[] = []

    for (const category of CATEGORIES) {
      const emojis = EMOJI_CATEGORIES[category as keyof typeof EMOJI_CATEGORIES]
      results.push(...emojis)
    }

    return results.slice(0, 100)
  }, [search, activeCategory])

  const copyEmoji = async (emoji: string) => {
    await navigator.clipboard.writeText(emoji)
    setCopied(emoji)
    setTimeout(() => setCopied(null), 1500)

    // Add to recent
    const newRecent = [emoji, ...recentEmojis.filter(e => e !== emoji)].slice(0, 20)
    setRecentEmojis(newRecent)
    localStorage.setItem('emojiRecent', JSON.stringify(newRecent))
  }

  const toggleFavorite = (emoji: string) => {
    const newFavorites = favorites.includes(emoji)
      ? favorites.filter(e => e !== emoji)
      : [...favorites, emoji]
    setFavorites(newFavorites)
    localStorage.setItem('emojiFavorites', JSON.stringify(newFavorites))
  }

  return (
    <div className="w-full max-w-2xl mx-auto">
      {/* Header */}
      <div className="text-center mb-6">
        <div className="inline-flex items-center gap-2 px-4 py-2 rounded-full bg-yellow-500/10 text-yellow-500 text-sm font-medium mb-4">
          <Smile className="w-4 h-4" />
          Utility
        </div>
        <h2 className="text-2xl font-bold">Emoji Picker</h2>
        <p className="text-muted-foreground mt-2">
          Browse and copy emojis to clipboard.
        </p>
      </div>

      {/* Search */}
      <div className="relative mb-4">
        <Search className="absolute left-4 top-1/2 -translate-y-1/2 w-5 h-5 text-muted-foreground" />
        <input
          type="text"
          value={search}
          onChange={(e) => setSearch(e.target.value)}
          placeholder="Search emojis..."
          className="w-full pl-12 pr-4 py-3 rounded-xl border border-border bg-background"
        />
      </div>

      {/* Categories */}
      <div className="flex gap-2 overflow-x-auto pb-2 mb-4">
        {recentEmojis.length > 0 && (
          <button
            onClick={() => { setActiveCategory('Recent'); setSearch(''); }}
            className={`px-3 py-1.5 rounded-lg text-sm whitespace-nowrap flex items-center gap-1 ${
              activeCategory === 'Recent'
                ? 'bg-primary text-primary-foreground'
                : 'bg-muted hover:bg-muted/80'
            }`}
          >
            <Clock className="w-3 h-3" />
            Recent
          </button>
        )}
        {favorites.length > 0 && (
          <button
            onClick={() => { setActiveCategory('Favorites'); setSearch(''); }}
            className={`px-3 py-1.5 rounded-lg text-sm whitespace-nowrap flex items-center gap-1 ${
              activeCategory === 'Favorites'
                ? 'bg-primary text-primary-foreground'
                : 'bg-muted hover:bg-muted/80'
            }`}
          >
            <Star className="w-3 h-3" />
            Favorites
          </button>
        )}
        {CATEGORIES.map(category => (
          <button
            key={category}
            onClick={() => { setActiveCategory(category); setSearch(''); }}
            className={`px-3 py-1.5 rounded-lg text-sm whitespace-nowrap ${
              activeCategory === category
                ? 'bg-primary text-primary-foreground'
                : 'bg-muted hover:bg-muted/80'
            }`}
          >
            {category}
          </button>
        ))}
      </div>

      {/* Emoji Grid */}
      <div className="grid grid-cols-8 sm:grid-cols-10 gap-1 p-4 rounded-xl border border-border bg-card max-h-[400px] overflow-y-auto">
        {(activeCategory === 'Recent' ? recentEmojis :
          activeCategory === 'Favorites' ? favorites :
          filteredEmojis
        ).map((emoji, idx) => (
          <motion.button
            key={`${emoji}-${idx}`}
            whileHover={{ scale: 1.2 }}
            whileTap={{ scale: 0.9 }}
            onClick={() => copyEmoji(emoji)}
            onDoubleClick={() => toggleFavorite(emoji)}
            className={`text-2xl p-2 rounded-lg hover:bg-muted transition-colors relative ${
              copied === emoji ? 'bg-green-500/20' : ''
            }`}
            title="Click to copy, double-click to favorite"
          >
            {emoji}
            {favorites.includes(emoji) && (
              <Star className="absolute -top-1 -right-1 w-3 h-3 text-yellow-500 fill-yellow-500" />
            )}
          </motion.button>
        ))}
      </div>

      {/* Copied Toast */}
      {copied && (
        <motion.div
          initial={{ opacity: 0, y: 20 }}
          animate={{ opacity: 1, y: 0 }}
          className="fixed bottom-8 left-1/2 -translate-x-1/2 px-4 py-2 bg-green-500 text-white rounded-lg flex items-center gap-2"
        >
          <Check className="w-4 h-4" />
          Copied {copied}
        </motion.div>
      )}

      {/* Info */}
      <div className="mt-6 p-4 rounded-lg bg-muted/30 text-sm text-muted-foreground text-center">
        <p>Click to copy. Double-click to add to favorites.</p>
      </div>
    </div>
  )
}
